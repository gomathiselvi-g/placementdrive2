package arrayconcept;

public class LenghtOfArray {
public static void main(String[] args) {
	String str[]= {"kavin","kabilan","Harish","salman","maideen"};
	System.out.println(str[0]);
	System.out.println(str.length);//syntax to print len of an array
	System.out.println(str[str.length-1]);//to print the last value
	System.out.println(str[str.length-2]);//to print the last value
	System.out.println(str[str.length-3]);//to print the last value
}
}
